package Test;

public class OpusRules {

	private int ruleSize;
	private String rule;
	private int numOfTran;
	private float leverage;
	private float lift;
	private double pVal;
	private float antSup;
	private float strength;
	public int getRuleSize() {
		return ruleSize;
	}
	public void setRuleSize(int ruleSize) {
		this.ruleSize = ruleSize;
	}
	public String getRule() {
		return rule;
	}
	public void setRule(String rule) {
		this.rule = rule;
	}
	public int getNumOfTran() {
		return numOfTran;
	}
	public void setNumOfTran(int numOfTran) {
		this.numOfTran = numOfTran;
	}
	public float getLeverage() {
		return leverage;
	}
	public void setLeverage(float leverage) {
		this.leverage = leverage;
	}
	public float getLift() {
		return lift;
	}
	public void setLift(float lift) {
		this.lift = lift;
	}
	public double getpVal() {
		return pVal;
	}
	public void setpVal(double pVal) {
		this.pVal = pVal;
	}
	public float getAntSup() {
		return antSup;
	}
	public void setAntSup(float antSup) {
		this.antSup = antSup;
	}
	public float getStrength() {
		return strength;
	}
	public void setStrength(float strength) {
		this.strength = strength;
	}
	
}
